package tryAndFinally;

/**
 *  TRY
 *  FINALLY
 *  NEXT
 */
public class goodTry {
    public static void main(String[] args) {
        try {
            System.out.println(" TRY ");
        }
        finally {
            System.out.println(" FINALLY ");
        }
        System.out.println(" NEXT ");
    }
}
